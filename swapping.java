public class swaping {
    public static void main(String[] args){
        int a = 11;
        int b = 10;
        System.out.println("Before swapping:");
        System.out.println("a="+a);
        System.out.println("b="+b);
        int temp;
        temp=a;
        a=b;
        b=temp;
        System.out.println("After swaping");
        System.out.println("a="+a);
        System.out.println("b="+b);
    }
    
}
